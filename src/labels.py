# クラスラベルIDから可視化時の色を取得
ID2COLOR = [
    (128,128,255),
    (128,255,255),
    (128,255,128),
    (255,255,128),
    (255,128,128),
    (255,128,255),
    (128,128,128),
    (128,192,255),
    (255,192,128),
    (192,128,255)
]

# 可視化時の色からクラスラベルIDを取得
def COLOR2ID(color):
    for i in range(0, len(ID2COLOR)):
        if color == ID2COLOR[i]:
            return i
    return -1
